                                    – Suka belajar hal &amp; tantangan baru<br>
                                    – Mampu berpikir kritis &amp; gesit<br>
                                    – Rajin sholat &amp; mengaji<br>
                                    – Suka lingkungan positif, semangat &amp; tidak mudah menyerah<br>
                                    – Berpengalaman di prioritaskan<br>
                                    – Pendidikan minimal SMA/SMK<br>
                                    – Memilihi pengalaman dibidang online shop, sosial media, marketing atau advertising diutamakan<br>
                                    – Pria/wanita muslim usia maks 25 tahun<br>
                                    <strong>Fasilitas:</strong><br>
                                    – Pendapatan menarik<br>
                                    – Workshop<br>
                                    – Jenjang karir<br>
                                    – Ruang kerja nyaman dan ber AC<br>
                                    – Wi-fi</p>
