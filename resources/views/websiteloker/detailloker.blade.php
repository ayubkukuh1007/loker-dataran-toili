                                    – Pendidikan minimal D3/S1<br>
                                    – Usia maks 27 tahun<br>
                                    <strong>Fasilitas</strong><br>
                                    – Pendapatan menarik<br>
                                    – Workshop<br>
                                    – Jenjang karir<br>
                                    – Ruang kerja nyaman dan ber AC<br>
                                    – Wi-fi</p>
